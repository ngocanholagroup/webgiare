    // 1. Script Scroll To Top
    const scrollBtn = document.getElementById('scrollToTopBtn');
    window.addEventListener('scroll', function() {
        if (window.scrollY > 300) {
            scrollBtn.classList.remove('translate-y-10', 'opacity-0', 'pointer-events-none');
            scrollBtn.classList.add('translate-y-0', 'opacity-100', 'pointer-events-auto');
        } else {
            scrollBtn.classList.add('translate-y-10', 'opacity-0', 'pointer-events-none');
            scrollBtn.classList.remove('translate-y-0', 'opacity-100', 'pointer-events-auto');
        }
    });

    // 2. Kích hoạt Lucide Icons
    // Lưu ý: Đảm bảo script lucide đã load ở Header hoặc load lại ở đây nếu cần
    if (typeof lucide !== 'undefined') {
        lucide.createIcons();
    }
    
    // 3. Xử lý logic Mobile Menu (Dùng lại logic của Header)
    // Đặt ở đây để đảm bảo DOM đã load xong
    document.addEventListener('DOMContentLoaded', function() {
        const menuBtn = document.getElementById('mobile-menu-btn');
        const mobileMenu = document.getElementById('mobile-menu');
        
        if(menuBtn && mobileMenu) {
            menuBtn.addEventListener('click', (e) => {
                e.stopPropagation(); // Ngăn sự kiện click lan ra ngoài
                mobileMenu.classList.toggle('hidden');
                
                // Đổi icon
                const icon = mobileMenu.classList.contains('hidden') ? 'menu' : 'x';
                menuBtn.innerHTML = `<i data-lucide="${icon}" class="w-7 h-7"></i>`;
                lucide.createIcons();
            });

            // Click ra ngoài thì đóng menu
            document.addEventListener('click', (e) => {
                if (!mobileMenu.contains(e.target) && !menuBtn.contains(e.target)) {
                    if (!mobileMenu.classList.contains('hidden')) {
                        mobileMenu.classList.add('hidden');
                        menuBtn.innerHTML = `<i data-lucide="menu" class="w-7 h-7"></i>`;
                        lucide.createIcons();
                    }
                }
            });
        }
    });
</script>

